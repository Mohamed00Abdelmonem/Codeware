# ======================== 8 kyu Reverse List Order=============================

def reverse_list(l):
    'return a list with the reverse order of l' 
    return l[::-1]
print(reverse_list([3, 4, 2,4,5,6,9]))


# Examples (Input -> Output)
# * [1, 2, 3, 4]  -> [4, 3, 2, 1]
# * [9, 2, 0, 7]  -> [7, 0, 2, 9]    